    private void muestraAdyacencia (){
        int bit = 0;
        String cad="  ";
        String nom = "";
        for (int k = 0; k<c; k++){
            if (k>0)
                cad="";
            System.out.print("    "+cad+ nombre[k]);
        }
        for (int k=0; k<c; k++){
            System.out.println("");
            for (int l=0; l<c; l++){
                bit =0;
                if (MAdyacencia[k][l])
                    bit=1;
                nom="";
                if (l==0)
                    nom = nombre[k] + "     ";
                System.out.print(nom + bit + "    ");
            }
        }
    }
